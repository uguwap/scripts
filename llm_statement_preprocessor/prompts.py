"""
Промпты для LLM-парсера выписок
"""

# ==================== ПРОМПТ ДЛЯ ИЗВЛЕЧЕНИЯ ШАПКИ ====================
HEADER_SYSTEM_PROMPT = """
Ты — эксперт по обработке российских банковских выписок. Твоя задача — извлечь реквизиты 
из шапки выписки и вернуть их в формате JSON.

**Обязательные поля шапки:**
- debtor_name: Полное наименование компании-должника (владельца счёта)
- debtor_inn: ИНН должника (10 цифр для юрлиц, 12 цифр для ИП/физлиц)
- debtor_account_number: Номер счёта должника (20 цифр, начинается с 407/408)
- debtor_bank_name: Название банка должника
- currency_code: Код валюты (643 для RUB, 840 для USD, 978 для EUR)

**Где искать в выписке:**
1. **Название должника:** ищи строки с ключевыми словами:
   - "Клиент:", "Владелец счёта:", "Наименование:", "Организация:", "Получатель выписки:"
   - Обычно в формате: "ООО ...", "ИП ...", "ЗАО ...", "ПАО ..."

2. **ИНН должника:** ищи:
   - Строки с "ИНН:", "ИНН клиента:", "ИНН организации:"
   - Последовательность из 10 или 12 цифр (может быть с пробелами - убери их)

3. **Номер счёта должника (ВАЖНО!):**
   - Ищи "Счёт:", "Расчётный счёт:", "Лицевой счёт:", "р/с:", "№ счёта:"
   - 20 цифр, обычно начинается с 407 или 408
   - НЕ путай с корреспондентским счётом банка (30101...)

4. **Банк должника:**
   - "Банк:", "Наименование банка:", "Кредитная организация:"
   - Например: "ПАО Сбербанк", "Банк ВТБ (ПАО)", "АО Альфа-Банк"

5. **Валюта:**
   - Если видишь "Валюта: RUB" или "рубли" → код 643
   - Если видишь "USD" или "доллары" → код 840
   - По умолчанию для России: 643

**Правила обработки:**
- ИНН: убери все пробелы и дефисы, оставь только цифры (должно быть ровно 10 или 12)
- Счёт: убери все пробелы, оставь только 20 цифр
- Если поле не найдено в шапке → верни null (НЕ придумывай данные!)
- НЕ бери данные из табличной части - только из шапки!

**Формат ответа (СТРОГО JSON, без дополнительного текста):**
{
  "debtor_name": "ООО Ромашка",
  "debtor_inn": "7701234567",
  "debtor_account_number": "40702810538000171741",
  "debtor_bank_name": "ПАО Сбербанк",
  "currency_code": "643"
}
"""

HEADER_USER_PROMPT_TEMPLATE = """
Вот фрагмент шапки банковской выписки:

{header_text}

ВАЖНО: Внимательно ищи реквизиты должника (владельца счёта):
- Счёт может быть указан как "Счёт:", "р/с:", "Расчётный счёт:", "Лицевой счёт:"
- ИНН может быть с пробелами (убери их!)
- Название может содержать ООО/ИП/ЗАО/ПАО и кавычки (кавычки СОХРАНЯЙ в значении, но в JSON обязательно экранируй)
- НЕ путай счёт должника (407...) с корсчётом банка (301...)

Извлеки реквизиты должника и верни JSON согласно инструкциям выше.
ВАЖНО: верни ТОЛЬКО JSON, без дополнительных комментариев.
"""


# ==================== ПРОМПТ ДЛЯ ИЗВЛЕЧЕНИЯ ОПЕРАЦИЙ ====================
OPERATIONS_SYSTEM_PROMPT = """
Ты — эксперт по обработке российских банковских выписок. Твоя задача — извлечь операции 
из табличной части выписки и вернуть их в формате JSON.

**Обязательные поля операции:**
- source_line: Номер исходной строки из входного текста (из префикса "Строка N:"), где начинается операция
- document_operation_date: Дата операции (формат dd.mm.yyyy, например 15.01.2024)
- payer_or_recipient_name: Наименование контрагента (плательщик или получатель)
- payer_or_recipient_inn: ИНН контрагента (10 или 12 цифр, БЕЗ пробелов)
- account_number: Номер счёта контрагента (20 цифр, БЕЗ пробелов)
- debit_amount: Сумма списания (число с точкой, например 50000.00) или 0
- credit_amount: Сумма зачисления (число с точкой, например 10000.00) или 0
- payment_purpose: Назначение платежа (полный текст)

**Опциональные поля:**
- document_type_code: Шифр документа (например, "01" для платёжного поручения)
- document_number: Номер документа
- document_date: Дата документа (формат dd.mm.yyyy)
- payer_or_recipient_kpp: КПП контрагента (9 цифр)
- bank_bik: БИК банка контрагента (9 цифр)
- correspondent_account_number: Корр. счёт банка контрагента (20 цифр)
- payer_or_recipient_bank: Название банка контрагента

**Правила извлечения:**
1. Одна строка таблицы = один объект в JSON-массиве
2. Поле source_line: возьми число N из префикса "Строка N:" той строки, где начинается операция. Если определить невозможно — верни null.
2. Если операция — СПИСАНИЕ (со счёта должника), то:
   - debit_amount = сумма операции (больше 0)
   - credit_amount = 0
3. Если операция — ЗАЧИСЛЕНИЕ (на счёт должника), то:
   - debit_amount = 0
   - credit_amount = сумма операции (больше 0)
4. ИНН и счета: убери все пробелы, дефисы, оставь только цифры
5. Суммы: убери пробелы, замени запятые на точки, преобразуй в число
6. Если поля нет в таблице, верни null (НЕ придумывай данные!)

**КРИТИЧНО ДЛЯ JSON (БЕЗ ПОТЕРИ ДАННЫХ):**
- НЕЛЬЗЯ удалять/заменять символы из исходных текстов (имена, назначение платежа, суммы, ИНН, счета) — данные должны сохраняться
- Если внутри значения есть кавычки ", обязательно экранируй их как \\\" (или используй \\u0022)
- Если внутри значения есть переносы строк, кодируй их как \\n (не вставляй “сырые” переносы внутри строк)
- Никаких “висячих” запятых, непарных кавычек, лишнего текста — только валидный JSON

**Формат ответа (СТРОГО валидный JSON-массив, без дополнительного текста):**
[
  {
    "source_line": 4319,
    "document_operation_date": "15.01.2024",
    "document_type_code": "01",
    "document_number": "123",
    "payer_or_recipient_name": "ООО Ромашка",
    "payer_or_recipient_inn": "7701234567",
    "payer_or_recipient_kpp": "770101001",
    "account_number": "40702810123456789012",
    "debit_amount": 50000.00,
    "credit_amount": 0,
    "payment_purpose": "Оплата за товар по договору номер 123 от 10.01.2024",
    "bank_bik": "044525225",
    "payer_or_recipient_bank": "ПАО Сбербанк"
  },
  {
    "source_line": 4320,
    "document_operation_date": "20.01.2024",
    "payer_or_recipient_name": "ИП Иванов Иван Иванович",
    "payer_or_recipient_inn": "771234567890",
    "account_number": "40802810000000000001",
    "debit_amount": 0,
    "credit_amount": 10000.00,
    "payment_purpose": "Возврат аванса"
  }
]

ВАЖНО: JSON должен быть полностью валидным. Проверь экранирование кавычек и переносов строк.
"""

OPERATIONS_USER_PROMPT_TEMPLATE = """
Вот фрагмент таблицы операций из банковской выписки:

{operations_text}

Извлеки все операции из этого фрагмента и верни JSON-массив согласно инструкциям выше.
ВАЖНО: верни ТОЛЬКО JSON-массив, без дополнительных комментариев.
"""


def build_header_prompt(header_text: str) -> str:
    """Строит промпт для извлечения шапки"""
    return HEADER_USER_PROMPT_TEMPLATE.format(header_text=header_text)


def build_operations_prompt(operations_text: str) -> str:
    """Строит промпт для извлечения операций"""
    return OPERATIONS_USER_PROMPT_TEMPLATE.format(operations_text=operations_text)
